class Solution {
public:
    int orangesRotting(vector<vector<int>>& grid) {
        int n = grid.size(), m = grid[0].size();
        vector<vector<bool>> vis(n, vector<bool>(m, false));
        queue<pair<int,pair<int,int>>> q;
        for(int i=0; i<n; i++){
            for(int j=0; j<m; j++){
                if(grid[i][j] == 2){
                    vis[i][j] = 0;
                    q.push({0, {i, j}});
                }
            }
        }
        int dr[4] = {1,0,-1,0};
        int dc[4] = {0,1,0,-1};
        int maxTime = 0;
        while(!q.empty()){
            int time = q.front().first;
            int r = q.front().second.first;
            int c = q.front().second.second;
            q.pop();
            maxTime = max(time, 0);
            for(int i=0; i<4; i++){
                int nr = r + dr[i];
                int nc = c + dc[i];
                if(nr < 0 || nr >= n || nc < 0 || nc >= m || grid[nr][nc] != 1) continue;
                q.push({time+1, {nr, nc}});
                vis[nr][nc] = 1;
                grid[nr][nc] = 2;
            }
        }
        for(int i=0; i<n; i++){
            for(int j=0; j<m; j++){
                if(grid[i][j] == 1){
                    return -1;
                }
            }
        }        return maxTime;
    }
};